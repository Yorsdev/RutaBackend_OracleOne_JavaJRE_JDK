
public class EjemploValoresVariables {
	public static void main(String[] args) {
		
		int n1 = 5;
		int n2 = 9;
		
		System.out.println(n2);
		
		n2 = 5;
		System.out.println(n2);
		
		n1 = 88;
		System.out.println(n2);
		
		//En java no se manejan punteros, solamente se va actualizando el valor actual de la variable.
	}
}
