
public class TIpoVariable {
	
	public static void main(String[] args) {
		
		double variable1 = 230.89;
		int variable1Entero = (int) variable1;//Cast Java, obliga la conversion del tipo de dato de una variable a gusto.
		
		System.out.println(variable1Entero);
		
		long numeroGrande = 2312312313123L; // 8 bytes
		short numeroPequenio = 223;// 2 bytes
		byte numeroAunMasPequenio = 2; // 1 byte
		float numeroDecimalPequenio = 2.5F; //4 bytes
	}
}
